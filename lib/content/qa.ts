/** 按分类获取问题 */
export function getQuestionsByCategory(category: QACategory): QAItem[] {
  return qaItems.filter(q => q.category === category);
}

/** 获取随机问题 */
export function getRandomQuestion(): QAItem {
  return qaItems[Math.floor(Math.random() * qaItems.length)];
}

/** 按分类获取随机问题 */
export function getRandomQuestionByCategory(category: QACategory): QAItem {
  const questions = getQuestionsByCategory(category);
  return questions[Math.floor(Math.random() * questions.length)];
}

/** 获取指定ID的问题 */
export function getQuestion(id: string): QAItem | undefined {
  return qaItems.find(q => q.id === id);
}

