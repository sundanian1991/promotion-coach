/** 完整版逐字稿 */
export const fullScript = scriptSegments.map(s => s.fullContent).join('\n\n---\n\n');

/** 骨架版逐字稿 */
export const skeletonScript = scriptSegments.map(s => s.skeletonContent).join('\n\n---\n\n');

/** 获取指定段落 */
export function getSegment(id: string): ScriptSegment | undefined {
  if (id === 'full') {
    // 返回完整版特殊段落
    return {
      id: 'full',
      title: '完整练习',
      duration: 480,
      fullContent: fullScript,
      skeletonContent: skeletonScript,
      keyPoints: ['完整版逐字稿', '骨架版速查'],
      dataPoints: [{ label: '建议时长', value: '8分钟' }],
      transition: '',
    };
  }
  return scriptSegments.find(s => s.id === id);
}

/** 获取下一段 */
export function getNextSegment(currentId: string): ScriptSegment | undefined {
  const index = scriptSegments.findIndex(s => s.id === currentId);
  if (index >= 0 && index < scriptSegments.length - 1) {
    return scriptSegments[index + 1];
  }
  return undefined;
}

/** 获取上一段 */
export function getPrevSegment(currentId: string): ScriptSegment | undefined {
  const index = scriptSegments.findIndex(s => s.id === currentId);
  if (index > 0) {
    return scriptSegments[index - 1];
  }
  return undefined;
}
